package shape;

import java.awt.Color;
import java.awt.Shape;

/**
 * This class will hold the information of a shape.
 * It will store the width, color and shape.
 * 
 * @author Armoni Atherton athera@uw.edu
 * @version November 5, 2017 
 */
public class ShapeInformation {
    
    /** The current color of the shape. */
    private Color myColor;
    
    /** The current width of the shape. */
    private Integer myWidth;
    
    /** The current type of shape. */
    private Shape myShape;
    
    /**
     * This class will hold all the information of the shape such 
     * as width to be painted, the type of shape and color.
     * 
     * @param theShape the incoming shape.
     * @param theColor the incoming color of the shape.
     * @param theWidth the incoming width of the shape.
     */
    public ShapeInformation(final Shape theShape, 
                            final Color theColor, 
                            final Integer theWidth) {
        myColor = theColor;
        myShape = theShape;
        myWidth = theWidth;
    }
    
    /**
     * This will get the color associated with the shape.
     * 
     * @return the current color of the shape.
     */
    public Color getColor() {
        return myColor;
    }
    
    /**
     * This will get the current shape.
     * 
     * @return the current shape.
     */
    public Shape getShape() {
        return myShape;
    }
 
    /**
     * This will get the width of the current shape.
     * 
     * @return the width of the shape.
     */
    public int getWidth() {
        return myWidth;
    }
    
    /**
     * This will set the shape to a new shape.
     * 
     * @param theShape the incoming shape to be changed to.
     */
    public void setShape(final Shape theShape) {
        myShape = theShape;
    }
    
    /**
     * This will set the color of the shape.
     * 
     * @param theColor the incoming color to be changed to.
     */
    public void setColor(final Color theColor) {
        myColor = theColor;
    }
    
    /**
     * This will set the current width of the shape to the new 
     * width.
     * 
     * @param theWidth the incoming width of the shape.
     */
    public void setWidth(final int theWidth) {
        myWidth = theWidth;
    }
    
}

